package com.example.demo.entity;

public class CinemaSeatKindInfo {
    public String seatkindno;

    public String seatkindname;

    public String price;

    public String getSeatkindno() {
        return seatkindno;
    }

    public void setSeatkindno(String seatkindno) {
        this.seatkindno = seatkindno;
    }

    public String getSeatkindname() {
        return seatkindname;
    }

    public void setSeatkindname(String seatkindname) {
        this.seatkindname = seatkindname;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }
}
